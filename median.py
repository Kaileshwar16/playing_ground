def median(nums):
    nums.sort()
    n = len(nums)
    mid = n // 2
    return (nums[mid] + nums[~mid]) / 2

print(median([7, 1, 3]))        # Output: 3
print(median([8, 4, 2, 10]))    # Output: 6
